function dayOfWeek(day) {
  let index;
  switch (day) {
    case "Monday":
      index = 1;
      console.log(index);
      break;
    case "Tuesday":
      index = 2;
      console.log(index);
      break;
    case "Wednesday":
      index = 3;
      console.log(index);
      break;
    case "Thursday":
      index = 4;
      console.log(index);
      break;
    case "Friday":
      index = 5;
      console.log(index);
      break;
    case "Saturday":
      index = 6;
      console.log(index);
      break;
    case "Sunday":
      index = 7;
      console.log(index);
      break;
    default:
      console.log("error");
  }
}
